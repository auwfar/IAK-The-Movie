public class Videos {
    String VideosName;
    String VideosType;
    String VideosImage;
    String VideosKey;

    public String getVideosName() {
        return VideosName;
    }

    public void setVideosName(String VideosName) {
        this.VideosName = VideosName;
    }

    public String getVideosType() {
        return VideosType;
    }

    public void setVideosType(String VideosType) {
        this.VideosType = VideosType;
    }

    public String getVideosImage() {
        return VideosImage;
    }

    public void setVideosImage(String VideosImage) {
        this.VideosImage = VideosImage;
    }

    public String getVideosKey() {
        return VideosKey;
    }

    public void setVideosKey(String videosKey) {
        VideosKey = videosKey;
    }
}
