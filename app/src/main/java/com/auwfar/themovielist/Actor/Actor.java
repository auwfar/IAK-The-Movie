public class Actor {
    String actorName;
    String actorImage;
    String actorAs;

    public String getActorName() {
        return actorName;
    }

    public void setActorName(String actorName) {
        this.actorName = actorName;
    }

    public String getActorImage() {
        return actorImage;
    }

    public void setActorImage(String actorImage) {
        this.actorImage = actorImage;
    }

    public String getActorAs() {
        return actorAs;
    }

    public void setActorAs(String actorAs) {
        this.actorAs = actorAs;
    }
}
